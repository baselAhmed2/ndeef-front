const dynamicTranslations: Array<[RegExp, (match: RegExpMatchArray) => string]> = [
  [/^(\d+)\s+services configured$/i, (match) => `${match[1]} خدمة مضافة`],
  [/^(\d+)\s+total orders$/i, (match) => `${match[1]} إجمالي الطلبات`],
  [/^(\d+)\s+total notifications$/i, (match) => `${match[1]} إجمالي الإشعارات`],
  [/^(\d+)\s+new$/i, (match) => `${match[1]} جديد`],
  [/^Unread\s+\((\d+)\)$/i, (match) => `غير المقروء (${match[1]})`],
  [/^Showing\s+(.+)\s+of\s+(.+)$/i, (match) => `عرض ${match[1]} من ${match[2]}`],
  [/^(\d+)\s+items$/i, (match) => `${match[1]} عناصر`],
  [/^(\d+)\s+orders$/i, (match) => `${match[1]} طلبات`],
  [/^(\d+)\s+notifications loaded(?:,\s+(\d+)\s+unread on this page)?$/i, (match) =>
    match[2] ? `${match[1]} إشعار محمل، ${match[2]} غير مقروء في هذه الصفحة` : `${match[1]} إشعار محمل`,
  ],
];

function normalizeText(value: string) {
  return value
    .replace(/\u00a0/g, " ")
    .replace(/[“”]/g, '"')
    .replace(/[’]/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function applyPhraseTranslations(value: string) {
  return Object.entries(arPhraseDictionary)
    .sort((a, b) => b[0].length - a[0].length)
    .reduce((text, [english, arabic]) => {
      if (english.length < 3) return text;
      return text.replace(new RegExp(escapeRegExp(english), "gi"), arabic);
    }, value);
}

export function translateText(value: string, locale: "en" | "ar") {
  if (locale === "en") return value;

  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const trimmed = normalizeText(value);
  if (!trimmed) return value;

  const exact = completeArDictionary[trimmed];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const [pattern, translate] of dynamicTranslations) {
    const match = trimmed.match(pattern);
    if (match) return `${leading}${translate(match)}${trailing}`;
  }

  const phraseTranslated = applyPhraseTranslations(trimmed);
  return `${leading}${phraseTranslated}${trailing}`;
}
